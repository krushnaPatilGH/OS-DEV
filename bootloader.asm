[BITS 16]
[ORG 0x7C00]


MOV SI, HELLO_STRING
CALL PRINTSTRING
JMP $

PRINTCHAR:
    MOV AH, 0x0E
    MOV BL, 0x07
    MOV BH, 0x00
    INT 0x10
    RET

PRINTSTRING:
    MOV AL, [SI]
    INC SI
    OR AL, AL
    JZ DONE
    CALL PRINTCHAR
    JMP PRINTSTRING
DONE:
    RET

;data 
HELLO_STRING db "hello how's everyone", 0
TIMES 510 - ($ - $$) db 0
DW 0xAA55